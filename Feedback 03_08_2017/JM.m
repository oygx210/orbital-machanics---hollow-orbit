function JM(L2,mu)
    r13=@(x,y) ((x+mu)^2+y^2)^(1/2);
    r23 = @(x,y) ((x+mu-1)^2+y^2)^(1/2);
    DerF = @(x,y,vx,vy,mu) [0, 0, 1, 0; 0, 0, 0,  1;...
        1-(1-mu)/r13(x,y)^3*(1-3*(x+mu)^2/r13(x,y)^2)-mu/r23(x,y)^3*(1-3*(x+mu-1)^2/r23(x,y)^2),...
        1+3*(1-mu)*(x+mu)*y/r13(x,y)^5+3*mu*(x+mu-1)*y/r23(x,y)^5,...
        0, 2;...
        3*(1-mu)*y*(x+mu)/r13(x,y)^5+mu*y*(x+mu-1)^2/r23(x,y)^5,...
        1-(1-mu)/r13(x,y)^3*(1-3*y^2/r13(x,y)^2)-mu/r23(x,y)^3*(1-3*y^2/r23(x,y)^2),...
        -2, 0];
    DerF(L2(1),L2(2),L2(3),L2(4),mu);
